
# Perform element-wise modulus operation on two numpy arrays.

import numpy as np

arr1 = np.array([[10], [20], [30], [40]])

print("Array1 is :\n",arr1)
arr2 = np.array([[2], [4], [5], [8]])
print("Array2 is :\n",arr2)

result = arr1 % arr2

print("Division of array1 and array2\n",result)
